# Emne 2 — Python Tal

# Eksempel 1: Helt basal regning
x, y = 10, 3
print("plus/minus/gange/dividere:", x+y, x-y, x*y, x/y)

# Eksempel 2: Heltalsdivision og rest
print("10 // 3 =", x//y, "| 10 % 3 =", x%y)

# Eksempel 3: Potenser
print("2**8 =", 2**8)

# Eksempel 4: Afrunding og absolut værdi
print("round(3.567,2):", round(3.567, 2), "| abs(-7):", abs(-7))

# Eksempel 5: Forskellige måder at vise tal på
value = 12345.6789
print(f"som heltal:{int(value)} | 2 decimaler:{value:.2f} | med kommaer:{value:,.1f}")
